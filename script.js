// Every 2
for(let i = 10; i>=0;i-=2){
    console.log(i)
}

// 9 to 3
for(let i =9; i>=3;i--){
    console.log(i)
}

// 9 Times Table
for(let i = 0; i<=10;i++){
    console.log(9*i)
}

// Even/Odd Reporter
for(let i = 1;i<=20;i++){
    if(i%2===0){
        console.log(`${i} is even`)
    }else{
        console.log(`${i} is odd`)
    }
}